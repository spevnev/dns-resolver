int main() {
    const char *DOMAIN
        = "aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa."
          "aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa."
          "aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa."
          "aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa";

    auto without_edns = MOCK_RESOLVER_CONFIG;
    without_edns.edns = FeatureState::Disable;

    auto with_edns = MOCK_RESOLVER_CONFIG;
    with_edns.edns = FeatureState::Require;

    Resolver resolver_without_edns{without_edns};
    auto opt_rrset = resolver_without_edns.resolve(DOMAIN, RRType::A);
    ASSERT(!opt_rrset.has_value());

    Resolver resolver{with_edns};
    opt_rrset = resolver.resolve(DOMAIN, RRType::A);
    ASSERT(opt_rrset.has_value());

    auto &rrset = opt_rrset.value();
    ASSERT(rrset.size() == 3);

    bool found1 = false;
    bool found2 = false;
    bool found3 = false;
    for (auto &rr : rrset) {
        ASSERT(rr.type == RRType::A);
        auto address = std::get<A>(rr.data).address;
        if (address == get_ip4("1.1.1.1")) found1 = true;
        if (address == get_ip4("2.2.2.2")) found2 = true;
        if (address == get_ip4("3.3.3.3")) found3 = true;
    }
    ASSERT(found1 && found2 && found3);

    return EXIT_SUCCESS;
}
